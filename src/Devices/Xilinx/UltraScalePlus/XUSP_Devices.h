#define XUSP_SETUP_DEVICE(prefix) {                                                               \
    XUSP_PARTNAME(prefix##NAME);                                                                  \
    XUSP_SLRNUM(prefix##SLRNUM);                                                                  \
    XUSP_ROWNUM(prefix##ROWNUM);                                                                  \
    XUSP_SLRFROMROW(0,0);                                                                         \
    XUSP_SLRTOROW(0,prefix##ROWNUM-1);                                                              \
    XUSP_IDCODE(0,prefix##IDCODE);                                                                \
}


