#define XUSP_SETUP_SLR_EXPANDED(slrID,idcodeVal,fromRowVal,toRowVal) {XUSP_IDCODE(slrID,idcodeVal);XUSP_SLRFROMROW(slrID,fromRowVal);XUSP_SLRTOROW(slrID,toRowVal);}
#define XUSP_SETUP_SLR0(prefix) {XUSP_SETUP_SLR_EXPANDED(0, prefix##IDCODE, prefix##SLR0_START, prefix##SLR0_END);}
#define XUSP_SETUP_SLR(slrID, prefix) {XUSP_SETUP_SLR_EXPANDED(slrID, prefix##IDCODE##slrID, prefix##SLR##slrID##_START, prefix##SLR##slrID##_END);}
#define XUSP_SETUP_SLRs_2(prefix) {XUSP_SETUP_SLR0(prefix); XUSP_SETUP_SLR(1,prefix);}
#define XUSP_SETUP_SLRs_3(prefix) {XUSP_SETUP_SLRs_2(prefix); XUSP_SETUP_SLR(2,prefix);}
